let num = parseInt(prompt("Introduce un número del 1 al 5:"));
switch(num) {
    case 1:
        console.log("Has elegido el número uno.");
        break;
    case 2:
        console.log("Has elegido el número dos.");
        break;
    case 3:
        console.log("Has elegido el número tres.");
        break;
    case 4:
        console.log("Has elegido el número cuatro.");
        break;
    case 5:
        console.log("Has elegido el número cinco.");
        break;
    default:
        console.log("Número no válido. Por favor");
 }

